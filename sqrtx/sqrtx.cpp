class Solution {
public:
    int mySqrt(int x) {
        long long ans = 0;
        long long start = 1;
        long long end = INT_MAX;
        
        while(start<=end){
            long long mid = start+(end-start)/2;
            
            if(mid*mid<=x){
                ans = mid;
                start = mid+1;
            }else{
                end = mid-1;
            }
        }
    return ans;
    }
};